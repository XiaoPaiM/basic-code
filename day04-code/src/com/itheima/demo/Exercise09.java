package com.heima.demo;

public class Exercise9 {
    public static void main(String[] args) {
        int n = 1;
        for (int i = 1; i <= 5; i++) {
            System.out.println(n);
            n++;
        }
        for(int i=1;i<=5;i++){
            n--;
            System.out.println(n);
        }
    }
}
